// Run Loop in Array

// Using For loop
let names = ['Akash', 'Batash', 'Hotash'];

console.log('Using For Loop:');
for (var i = 0; i < names.length; i++) {
        var name = names[i];
        console.log(name);
}


// Using While Loop

console.log('Using While Loop:');
var i = 0;
while (i < names.length) {
        var name = names[i];
        i++;
        console.log(name);
}